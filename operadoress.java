
public class operadoress {
	public static void main(String[] args) {
		
		int numero = 10;
		
		System.out.println("atribuição_composta");
		numero += 4;
		System.out.println(numero);
		
		numero -= 7;
		System.out.println(numero);
		
		numero *= 6;
		System.out.println(numero);
		
		numero /= 2;
		System.out.println(numero);
		
	}

}
