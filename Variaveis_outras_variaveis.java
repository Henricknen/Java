
public class Variaveis_outras_variaveis {
	public static void main(String[] args) {
		System.out.println("Variavel_Boolean");
		boolean varBoolean = true;		// Boolean 1 bit
		
		System.out.println(varBoolean); 
		
		
		System.out.println("Variavel_Char");
		char simbolo = 'a';
		System.out.println(simbolo);
		simbolo = 97;											// vai ter mesmo valor da primeir variavel "a", pois trabalha com a tabela ASCII
		System.out.println(simbolo);
		
		
		System.out.println("Variavel_String");
		String palavra = "Luis henrique silva ferreira";			// inicia com o nome da string em maiuscula
		System.out.println(palavra);
		
	}

}
