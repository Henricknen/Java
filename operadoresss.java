
public class operadoresss {
	public static void main(String[] args) {
		
		System.out.println(" 1 é igual 2");
		System.out.println(1 == 2);		
		
		System.out.println("1 é diferente de 2");
		System.out.println(1 != 2);
		
		System.out.println("1 é maior 2");
		System.out.println(1 > 2);
		
		System.out.println("1 é menor 2");
		System.out.println(1 < 2);
		
		System.out.println("1 é maior ou igual a 1");
		System.out.println(1 >= 1);
		
	}

}
